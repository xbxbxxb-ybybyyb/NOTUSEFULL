def trading_day(start_date, end_date, freq='D') -> list:
    """
    :param start_date: 起始日期
    :param end_date: 结束日期
    :param freq: 查询频率。默认是日频（'D'）。
                  如查询月频（'M'），则仿照Wind的“插入日期”的规则，输出start_date和end_date之间每个月末的最后一个
                  交易日，如end_date不是当月的最后一个交易日，那么再加上end_date（含end_date）前的最后一个交易日；
                  如start_date和end_date同属一个月、且其中包含超过一个交易日，那么输出之间的最后一个交易日。
                  参考以下3个例子：其中20181108和20181109是周四周五、是交易日；20181110和20181111是周末、不是交易日。
                  例1: t_d0 = trading_day(20181110, 20181111, 'M')  --- t_d0 = []
                  例2: t_d1 = trading_day(20181108, 20181111, 'M')  --- t_d1 = [20181109]
                  例3: t_d2 = trading_day(20181001, 20181111, 'M')  --- t_d2 = [20181031, 20181109]
    :return: 输出list，已按升序排列
    """
    if start_date == end_date:
        if start_date in trading_day_list:
            answer_list = [start_date]
        else:
            answer_list = []
    elif start_date > end_date:
        answer_list = []
    else:
        if freq == 'D':
            answer_list = [x for x in trading_day_list if start_date <= x <= end_date]
            answer_list.sort()
        elif freq == 'M':
            answer_list = [x for x in trading_day_list_monthly if start_date <= x <= end_date]
            answer_list_daily = [x for x in trading_day_list if start_date <= x <= end_date]
            if isinstance(answer_list, int):
                answer_list = [answer_list]
            # 如end_date不是当月的最后一个交易日，那么再加上end_date（含end_date）前的最后一个交易日
            if answer_list.__len__() > 0 and answer_list_daily[-1] > answer_list[-1]:
                answer_list.append(answer_list_daily[-1])
            # 如start_date和end_date同属一个月、且其中包含超过一个交易日，那么输出之间的最后一个交易日
            elif answer_list.__len__() == 0 and answer_list_daily.__len__() > 0:
                answer_list = [answer_list_daily[-1]]
            # 如start_date和end_date在同一个月内、且中间无交易日，则输出空list
            elif answer_list.__len__() == 0 and answer_list_daily.__len__() == 0:
                answer_list = []
            answer_list.sort()
        else:
            answer_list = []
    return answer_list


